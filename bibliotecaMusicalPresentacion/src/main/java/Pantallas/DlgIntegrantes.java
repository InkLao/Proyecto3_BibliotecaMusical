    public void cargarDatos(List<String> integrantes){
    
    // Lista de datos din√°micos
        List<String> datos = integrantes;

        // Construir texto HTML
        StringBuilder textoHTML = new StringBuilder("<html>");
        for (String dato : datos) {
            textoHTML.append(dato).append("<br>");
        }
        textoHTML.append("</html>");
        
        jblIntegrantes.setText(textoHTML.toString());
    }
    
