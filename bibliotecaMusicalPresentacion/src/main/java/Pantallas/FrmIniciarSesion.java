    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        String correo = txtCorreo.getText().trim();
    String contra = new String(txtContrasena.getPassword()).trim();

    if (correo.isEmpty() || contra.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Correo y contrase침a son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        IUsuarioNegocio negocio = new UsuarioNegocio();
        UsuarioDTO usuarioDTO = negocio.buscarUsuarioIniciarSesion(correo, contra);

        JOptionPane.showMessageDialog(this, "Inicio de sesi칩n exitoso.");
        FrmUsuarioPerfil perfil = new FrmUsuarioPerfil(this, usuarioDTO);
        perfil.setVisible(true);
        this.dispose();
    } catch (IllegalArgumentException ex) {
        JOptionPane.showMessageDialog(this, ex.getMessage(), "Error de autenticaci칩n", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnIngresarActionPerformed


