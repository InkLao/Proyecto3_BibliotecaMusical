    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String nombreGenero = txtGenero.getText().trim();
        if (nombreGenero.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El campo de género no puede estar vacío.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            GeneroNoDeseadoNegocio negocio = new GeneroNoDeseadoNegocio();
            negocio.agregarGeneroNoDeseado(new GeneroNoDeseadoDTO(null, usuarioDTO.getId(), List.of(nombreGenero)));

            cargarGeneros(); // Refrescar la lista
            txtGenero.setText(""); // Limpiar el campo de texto
            JOptionPane.showMessageDialog(this, "Género agregado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar el género: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

