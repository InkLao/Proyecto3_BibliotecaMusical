    /**
     * Método para cargar la lista de géneros no deseados desde la base de
     * datos.
     */
    private void cargarGeneros() {
        try {
            GeneroNoDeseadoNegocio negocio = new GeneroNoDeseadoNegocio();
            List<String> generos = negocio.obtenerGenerosNoDeseados(usuarioDTO.getId());
            lstGenerosNoDeseados.setListData(generos.toArray(new String[0]));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar los géneros: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Configurar la imagen del bote de basura y su acción al hacer clic.
     */
    private void configurarImagenBote() {
        try {
            // Cargar la imagen desde el classpath
            ImageIcon icon = new ImageIcon(getClass().getResource("/BasuraLogo.png"));
            if (icon.getImage() == null) {
                throw new IllegalArgumentException("No se pudo cargar la imagen.");
            }

            // Ajustar el tamaño de la imagen
            Image img = icon.getImage().getScaledInstance(40, 40, Image.SCALE_SMOOTH);
            lblImagenBote.setIcon(new ImageIcon(img));

            // Añadir evento para eliminar género al hacer clic
            lblImagenBote.addMouseListener(new java.awt.event.MouseAdapter() {
                public void mouseClicked(java.awt.event.MouseEvent evt) {
                    eliminarGeneroSeleccionado();
                }
            });
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar la imagen del bote: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Método para eliminar el género seleccionado de la lista.
     */
    private void eliminarGeneroSeleccionado() {
        String generoSeleccionado = lstGenerosNoDeseados.getSelectedValue();
        if (generoSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "Seleccione un género para eliminar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            GeneroNoDeseadoNegocio negocio = new GeneroNoDeseadoNegocio();
            negocio.eliminarGenero(usuarioDTO.getId(), generoSeleccionado);

            // Refrescar la lista después de la eliminación
            cargarGeneros();
            JOptionPane.showMessageDialog(this, "Género eliminado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar el género: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

